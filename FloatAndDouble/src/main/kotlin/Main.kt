fun main() {
    val number1 = 2.5    // This is by default double data type
    val number2: Float = 3.5f    // Suffixing 'f' or 'F' is needed for float type value
    val number3 = 4f
    val number4: Double = 5.0 // Decimal point is necessary when we explicitly mention the type of the variable

    println("number1: $number1")
    println("number2: $number2")
}